def map_all(maze_map):
    
    coords_of_map = []
    xcoord_of_map = -1

    for string in (list(maze_map)):
        for key, value in enumerate(string):
            if key == 0:
                xcoord_of_map += 1
            if value == 0:
                coordinate = (xcoord_of_map, key)
                coords_of_map.append(((coordinate), True))
            elif value == 1:
                coordinate = (xcoord_of_map, key)
                coords_of_map.append(((coordinate), False))
                
    return coords_of_map


def get_neighbours(coords_of_map, position):

    neigh_position = []

    for neighbours in coords_of_map:
        coord, not_bush = neighbours
        x, y = coord
        xp, yp = position
        
        if x == xp and y == yp+1 and not_bush == True:
            direction = "E"
            neigh_position.append(((x,y), direction))
        elif x == xp and y == yp-1 and not_bush == True:
            direction = "W"
            neigh_position.append(((x,y), direction))
        elif x == xp+1 and y == yp and not_bush == True:
            direction = "S"
            neigh_position.append(((x,y), direction))
        elif x == xp-1 and y == yp and not_bush == True:
            direction = "N"
            neigh_position.append(((x,y), direction))
            

    return neigh_position


